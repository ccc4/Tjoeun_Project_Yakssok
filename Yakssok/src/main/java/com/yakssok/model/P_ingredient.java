package com.yakssok.model;

public class P_ingredient {
	private int p_ingredient_idx;
	private int p_idx;
	private String eng;
	private String kor;
	private String content;
	
	public int getP_ingredient_idx() {
		return p_ingredient_idx;
	}
	public void setP_ingredient_idx(int p_ingredient_idx) {
		this.p_ingredient_idx = p_ingredient_idx;
	}
	public int getP_idx() {
		return p_idx;
	}
	public void setP_idx(int p_idx) {
		this.p_idx = p_idx;
	}
	public String getEng() {
		return eng;
	}
	public void setEng(String eng) {
		this.eng = eng;
	}
	public String getKor() {
		return kor;
	}
	public void setKor(String kor) {
		this.kor = kor;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	
	
}
